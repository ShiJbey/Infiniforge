/**
 * Generates a katana blade collar known as a Habaki.
 * Habaki are placed above the guard and are usually
 * trapezoidal in shape.
 */
export default class HabakiFunction {
  /** Width of the blade from cutting edge to spine (m) */
  public bladeWidth = 0.04;
  /** Thickness of the blade from one flat side to the other (m) */
  public bladeThickness = 0.005;
  /** Height of the generated Habaki (m) */
  public height = 0.03;
}
